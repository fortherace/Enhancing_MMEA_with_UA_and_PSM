function [X, F] = run_mmeawi_mmf1(N, maxFE)
    clc;

    %% 添加 PlatEMO 路径
    addpath(genpath('D:\Project\PlatEMO\PlatEMO\PlatEMO'));

    N = double(N);
    maxFE = double(maxFE);

    %% 调用 PlatEMO
    [X, F] = platemo( ...
        'algorithm', @MMEAWI, ...
        'problem',   @MMF1, ...
        'N',         N, ...
        'maxFE',     maxFE, ...
        'draw',      false, ...
        'silent',    true ...
    );
end
